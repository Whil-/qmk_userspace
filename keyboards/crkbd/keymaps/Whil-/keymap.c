bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    /* KEYBOARD PET STATUS START */
  case CTL_TAB:
  case GUI_QUOT:
  case CTL_QUOT:
#ifdef OLED_ENABLE
    isSneaking = record->event.pressed;
#endif
    break;

  case KC_SPC:
  case RSE_SPC:
#ifdef OLED_ENABLE
    if (record->tap.count) {
      isJumping = record->event.pressed;
    }
    if (isJumping) {
      showedJump = false;
    }
#endif
    break;
  case KC_CAPS:
#ifdef OLED_ENABLE
    isBarking = record->event.pressed;
#endif
    break;
    /* KEYBOARD PET STATUS END */

    /* Custom SWEDISH Keycodes START */
  case SWE_AA:
  case SWE_AE:
  case SWE_OE:
    if (record->event.pressed) {
      uint8_t mods = get_mods();
      clear_mods();

      // Send code based on which key was pressed and whether Shift was held.
      uint16_t index = keycode - SWE_AA;
      uint8_t shift = mods & (MOD_BIT(KC_LSFT) | MOD_BIT(KC_RSFT));
      uint8_t unicode_mode = get_unicode_input_mode();
      if (unicode_mode == UNICODE_MODE_MACOS) {
        send_string(swe_mac_US_codes[index][(bool)shift]);
      } else if (unicode_mode == UNICODE_MODE_WINDOWS) {
        send_string(swe_win_alt_codes[index][(bool)shift]);
      } else {
        register_unicode(swe_unicodes[index][(bool)shift]);
      }
      set_mods(mods);
    }
    return false;
    break;
  case GUI_AE:
  case CTL_AE:
    if (record->tap.count && record->event.pressed) {
      uint8_t mods = get_mods();
      clear_mods();

      // Send code based on which key was pressed and whether Shift was held.
      uint8_t shift = mods & (MOD_BIT(KC_LSFT) | MOD_BIT(KC_RSFT));
      uint8_t unicode_mode = get_unicode_input_mode();
      if (unicode_mode == UNICODE_MODE_MACOS) {
        send_string(swe_mac_US_codes[1][(bool)shift]);
      } else if (unicode_mode == UNICODE_MODE_WINDOWS) {
        send_string(swe_win_alt_codes[1][(bool)shift]);
      } else {
        register_unicode(swe_unicodes[1][(bool)shift]);
      }
      set_mods(mods);
      return false;        // Return false to ignore further processing of key
    }
    break;
  }
  return true;
}
